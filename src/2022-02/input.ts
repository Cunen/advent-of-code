type RPS = "R" | "P" | "S";
type XYZ = "X" | "Y" | "Z";

const getRPS = (rps: RPS, xyz: XYZ): RPS => {
  // Tie
  if (xyz === "Y") return rps;
  // Need To Lose
  const loss: Record<RPS, RPS> = { R: "S", P: "R", S: "P" };
  if (xyz === "X") return loss[rps];
  // Need to win
  const win: Record<RPS, RPS> = { R: "P", P: "S", S: "R" };
  return win[rps];
};

export const roundsOfRPS = prodInput.split("\n").map((line) => {
  const [opponent, you] = line.split(" ");
  const opponentRPS: Record<string, "R" | "P" | "S"> = {
    A: "R",
    B: "P",
    C: "S",
  };

  const youRPS: Record<string, "R" | "P" | "S"> = { X: "R", Y: "P", Z: "S" };

  const pointsRecord: Record<"R" | "P" | "S", number> = { R: 1, P: 2, S: 3 };

  const o = opponentRPS[opponent];
  const y = youRPS[you];
  const y2 = getRPS(o, you as XYZ);

  const win =
    (y === "R" && o === "S") ||
    (y === "P" && o === "R") ||
    (y === "S" && o === "P");
  const tie = y === o;
  const points = (win ? 6 : tie ? 3 : 0) + pointsRecord[y];

  const win2 =
    (y2 === "R" && o === "S") ||
    (y2 === "P" && o === "R") ||
    (y2 === "S" && o === "P");
  const tie2 = y2 === o;
  const points2 = (win2 ? 6 : tie2 ? 3 : 0) + pointsRecord[y2];

  return {
    opponent: opponentRPS[opponent],
    youRPS: youRPS[you],
    points,
    points2,
  };
});
